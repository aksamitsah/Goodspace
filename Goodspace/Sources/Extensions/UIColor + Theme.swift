import UIKit.UIColor


extension UIColor{
    static let primaryColor = UIColor(named: "ColorPrimary") ?? UIColor()
    static let secondaryColor = UIColor(named: "ColorSecondary") ?? UIColor()
    
    static let darkBlueColor = UIColor(named: "ColorDarkBlue") ?? UIColor()
    static let greenColor = UIColor(named: "ColorGreen") ?? UIColor()
    static let lightPinkColor = UIColor(named: "ColorLightPink") ?? UIColor()
}
